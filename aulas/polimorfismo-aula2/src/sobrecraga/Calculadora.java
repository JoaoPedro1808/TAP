package sobrecraga;

public class Calculadora {
    /**
     *
     * @param a int
     * @param b int
     * @return int
     */
    public int calculo(final int a, final int b) {
        return a + b;
    }
    /**
     *
     * @param a int
     * @param b int
     * @param c int
     * @return int
     */
    public int calculo(final int a, final int b, final int c) {
        return a + b + c;
    }
    /**
     *
     * @param a double
     * @param b double
     * @return double
     */
    public double calculo(final double a, final double b) {
        return a + b;
    }
}
